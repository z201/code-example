package cn.z201.junit4.tool;

public class Calculate {
	
	//相加
	public int add(int a , int b){
		return a + b;
	}
	
	//相减
	public int subtract(int a , int b){
		return a - b;
	}
	
	//相乘
	public int multipy(int a , int b){
		return a * b;
	}
	
	//相除
	public int divide(int a , int b){
		return a / b;
	}

}
